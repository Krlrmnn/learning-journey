### Operators ###

print(3 + 4) # addition
print(3 - 4) # subtraction
print(3 * 4) # multiplication
print(12 / 4) # division always returns a float
print(12 % 4) # modulus operator (remainder of the division)
print (13 // 4) # floor division
print (2 ** 3) # 2 to the power of 3

print("Hi" + " Python")
print("hi" + str(3))

print(5 ** 2 // 3 + 4 * 2 - (6 / 3)) # order of operations
my_float = 2.5
print("hola" * int(my_float))

my_int = 5
print("hola" + str(my_int))

# Comparative operators

print(3 > 4)
print(3 < 4)
print(3 <= 5)
print(3 >= 5)
print(3 == 3)
print(3 != 3)

# logical operators

print(3 > 2 and 2 < 3) # and
print(3 > 2 or 2 > 3) # or
print(not(3 > 2)) # not